int gs_file::Read(FILE *f)
{
	//=================================================================
	//== Read one record
	//=================================================================

	return fscanf(f,"%d%d%d%d%ld%s"
		,&kfile
		,&ktape
		,&kthead
		,&kfhead
		,&ksize
		,fname);
} //-------------------------------------------------------------------

